def test_l1_cli_run_is_stored_as_declaration_and_cannot_be_promoted_to_l2(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch,
) -> None:
    _repo(tmp_path)
    _use_repo_schemas(tmp_path, monkeypatch)
    task_id = _task(tmp_path)
    _ready_work_unit(tmp_path, task_id)

    completed = _run_cli(
        tmp_path,
        "run", "register", task_id,
        "--work-unit-id", WORK_UNIT_ID,
        "--context-id", "declared-context",
        "--actor", "declared-reviewer",
        "--independence-level", "L1",
        "--expected-revision", "1",
        "--idempotency-key", "declared-l1",
    )

    assert completed.exit_code == 0, completed.exception
    run = json.loads(completed.stdout)["run"]
    assert set(run["runtime"]) == {"profile", "execution_context_id"}
    assert "independence_attestation" not in run
    persisted = {**run, "status": "succeeded"}
    review = {
        "kind": "review",
        "run_id": run["id"],
        "review": {"independence_level": "L2", "reviewed_diff_digest": "sha256:" + "a" * 64},
    }
    decision = evaluate_review_independence(
        review,
        persisted,
        [],
        current_diff_digest="sha256:" + "a" * 64,
        minimum_level="L2",
        runtime_profiles={"local-single": {"capabilities": {"read_only_run": "native"}}},
        verified_run_ids=set(),
    )
    assert {"REVIEW_LEVEL_UNATTESTED", "REVIEW_PROVENANCE_UNVERIFIED"} <= set(decision.codes)


def test_verified_l2_run_event_is_traceable_and_direct_spoof_is_rejected(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch,
) -> None:
    _repo(tmp_path)
    _use_repo_schemas(tmp_path, monkeypatch)
    task_id = _task(tmp_path)
    work_unit = _ready_work_unit(tmp_path, task_id)
    runtime = {
        "profile": "local-single",
        "execution_context_id": "verified-review-context",
        "provider": "external-runtime",
        "model": "review-model",
        "read_only": True,
        "commit_participation": [],
    }
    context = _authority("verified-reviewer", runtime=runtime)
    fields = resolve_run_registration(
        frozen_profile_id="local-single",
        declared_profile="local-single",
        declared_context_id="verified-review-context",
        declared_actor="verified-reviewer",
        declared_actor_kind="agent",
        declared_independence_level="L2",
        declared_provider="external-runtime",
        declared_model="review-model",
        declared_read_only=True,
        declared_commit_participation=[],
        authority_context=context,
    )
    run_id = "RUN-01K0W4Z36K3W5C2R0A3M8N9P7T"
    run = {
        "schema_version": 1, "id": run_id, "task_id": task_id, "work_unit_id": WORK_UNIT_ID,
        "status": "running", **fields, "started_at": "2026-07-20T00:00:00Z",
        "finished_at": None, "exit_code": None,
    }
    repository = FilesystemTaskRepository(tmp_path)
    run_path = repository.task_dir(task_id) / "runs" / f"{run_id}.json"
    event = repository.append_event(
        task_id, "run_started", {"run_id": run_id, "run": run, "work_unit_id": WORK_UNIT_ID, "work_unit": work_unit},
        actor=context.actor(), expected_revision=1, idempotency_key="verified-run", run_id=run_id,
        materializations=[(run_path, run)], authority_context=context,
    ).event
    assert verified_entity_ids([event], "run_id") == {run_id}

    spoof_id = "RUN-01K0W4Z36K3W5C2R0A3M8N9P7V"
    spoof = {**run, "id": spoof_id}
    with pytest.raises(MacError) as caught:
        repository.append_event(
            task_id, "run_started", {"run_id": spoof_id, "run": spoof},
            actor=spoof["actor"], expected_revision=2, idempotency_key="spoofed-run",
            materializations=[(repository.task_dir(task_id) / "runs" / f"{spoof_id}.json", spoof)],
        )
    assert caught.value.code == "PROVENANCE_UNVERIFIED"
    assert not (repository.task_dir(task_id) / "runs" / f"{spoof_id}.json").exists()


def test_direct_privileged_event_and_historical_self_reported_approval_fail_closed(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch,
) -> None:
    _repo(tmp_path)
    _use_repo_schemas(tmp_path, monkeypatch)
    task_id = _task(tmp_path)
    repository = FilesystemTaskRepository(tmp_path)
    with pytest.raises(TypeError):
        VerifiedAuthorityContext(
            "backend-owner", "human", "forged", "sha256:" + "0" * 64,
        )
    with pytest.raises(MacError) as caught:
        repository.append_event(
            task_id, "scope_approved", {"approval_id": "APR-01K0W4Z36K3W5C2R0A3M8N9P81"},
            actor={"id": "backend-owner", "kind": "human"}, expected_revision=0,
            idempotency_key="direct-spoof",
        )
    assert caught.value.code == "EXTERNAL_AUTHORITY_REQUIRED"

    task_dir = repository.task_dir(task_id)
    scope = load_data(task_dir / "scope-contract.yaml")
    scope["status"] = "approved"
    scope["approved_by"] = ["backend-owner"]
    atomic_write_yaml(task_dir / "scope-contract.yaml", scope)
    approval = {
        "schema_version": 1, "id": "APR-01K0W4Z36K3W5C2R0A3M8N9P81", "task_id": task_id,
        "kind": "scope", "actor": {"id": "backend-owner", "kind": "human"},
        "decision": "approved", "subject_ref": f"tasks/{task_id}/scope-contract.yaml",
        "independence_level": "L1", "recorded_at": "2026-07-20T00:00:00Z",
    }
    atomic_write_json(task_dir / "approvals" / f"{approval['id']}.json", approval)
    decision = evaluate_repository_close(tmp_path, task_id, "backend-owner")
    assert "CLOSE_SCOPE_APPROVAL_INVALID" in decision.codes
