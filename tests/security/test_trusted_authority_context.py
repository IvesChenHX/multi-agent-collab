def test_verified_application_scope_approval_is_event_first_and_redacted(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch,
) -> None:
    _repo(tmp_path)
    _use_repo_schemas(tmp_path, monkeypatch)
    task_id = _task(tmp_path)
    context = _authority()

    result = record_verified_scope_approval(
        tmp_path,
        task_id,
        declared_actor="backend-owner",
        independence_level="L1",
        expected_revision=0,
        idempotency_key="verified-approval",
        authority_context=context,
    )

    event = result["event"]
    assert event["new_revision"] == 1
    assert load_data(tmp_path / "tasks" / task_id / "task.yaml")["revision"] == 1
    assert load_data(tmp_path / "tasks" / task_id / "scope-contract.yaml")["status"] == "approved"
    assert (event["payload"] or {})["verified_authority"] == {
        "actor_id": "backend-owner",
        "source": "test-external-verifier",
        "assertion_digest": context.assertion_digest,
    }
    event_text = (tmp_path / "tasks" / task_id / "events" / f"{event['event_id']}.json").read_text(encoding="utf-8")
    assert "raw_credential" not in event_text and "must-not-persist" not in event_text
    assert verified_entity_ids([event], "approval_id") == {result["approval"]["id"]}
    assert "CLOSE_SCOPE_APPROVAL_INVALID" not in evaluate_repository_close(
        tmp_path, task_id, "backend-owner",
    ).codes


