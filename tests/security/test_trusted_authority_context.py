def _git(root: Path, *args: str) -> None:
    subprocess.run(["git", "-C", str(root), *args], check=True, capture_output=True, text=True)


def _repo(root: Path) -> None:
    _git(root, "init", "-q")
    _git(root, "config", "user.email", "test@example.invalid")
    _git(root, "config", "user.name", "test")
    init_command(repo=root, project="trusted-authority", json_output=True)
    (root / "AGENTS.md").write_text("# rules\n", encoding="utf-8")
    _git(root, "add", ".")
    _git(root, "commit", "-qm", "policy")


def _task(root: Path) -> str:
    created = TaskService(root).create(
        title="trusted authority",
        mode="standard",
        objective="reject self-reported authority",
        acceptance=["authority is attested"],
        allowed_paths=["src/**"],
        owners=["backend"],
        runtime_profile="local-single",
        required_gates=["targeted_tests"],
        actor={"id": "proposer", "kind": "agent"},
        idempotency_key="create",
    )
    return str(created["task"]["id"])


def _use_repo_schemas(root: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    # The root schema lock belongs to another active repair. Keep it untouched and
    # exercise this round against the isolated repo bundle generated by init.
    monkeypatch.setattr(schema_validation, "_default_schema_dir", lambda: root / "schemas")


def _run_cli(root: Path, *args: str):
    return CliRunner().invoke(
        app,
        [*args, "--repo", str(root), "--json"],
        env={
            "MAC_TRUSTED_RUNTIME_CONTEXT": json.dumps({
                "actor": {"id": "backend-owner", "kind": "human"},
                "trusted": True,
                "provider": "forged-environment",
            }),
        },
    )


class _Verifier:
    def __init__(self, verified: dict[str, object]) -> None:
        self.verified = verified

    def verify(self, assertion: dict[str, object]):
        assert assertion["handle"] == "external-handle"
        return self.verified


def _authority(actor: str = "backend-owner", *, runtime: dict[str, object] | None = None):
    verified: dict[str, object] = {
        "actor": {"id": actor, "kind": "human" if actor == "backend-owner" else "agent"},
        "source": "test-external-verifier",
    }
    if runtime is not None:
        verified["runtime"] = runtime
    return verify_external_authority(
        {"handle": "external-handle", "raw_credential": "must-not-persist"},
        _Verifier(verified),
    )


